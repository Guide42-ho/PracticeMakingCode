public class For_While_DoWhileOneToHundred {
    public static void main(String args[]) {
    int i = 0; 
        //// For 
        for (i = 1;  i <= 100 ; i++) { 
 
        System.out.println(i); 
        } 
 
        /// While 
        while (i <= 100) { 
         
        System.out.println(i); 
        i++; 
        }  
  
        /// Do While 
        do { 
         System.out.println(i); 
        i++; 
         
        } while ( i<=100 ); 
    }

}